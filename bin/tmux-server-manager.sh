#!/bin/bash
# =============================================================================
# tmux Server Manager
# =============================================================================
# A unified tool for setting up a Mac as a Tailscale-only SSH server
# with tmux session management capabilities.
#
# Features:
#   - Configure SSH to accept connections ONLY over Tailscale
#   - pf firewall rules as backup protection
#   - tmux session management for remote work
#
# Author: Created for remote Mac server management
# License: MIT
# =============================================================================

set -e

SCRIPT_NAME=$(basename "$0")
VERSION="1.0.0"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m'

# =============================================================================
# Helper Functions
# =============================================================================

print_header() {
    echo -e "${CYAN}╔════════════════════════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}║${NC}  ${BOLD}tmux Server Manager${NC} v${VERSION}                                   ${CYAN}║${NC}"
    echo -e "${CYAN}╚════════════════════════════════════════════════════════════════╝${NC}"
    echo ""
}

log_info() {
    echo -e "${BLUE}ℹ${NC}  $1"
}

log_success() {
    echo -e "${GREEN}✓${NC}  $1"
}

log_warn() {
    echo -e "${YELLOW}⚠${NC}  $1"
}

log_error() {
    echo -e "${RED}✗${NC}  $1"
}

require_tailscale() {
    if ! command -v tailscale &> /dev/null; then
        log_error "Tailscale is not installed"
        echo "    Install from: https://tailscale.com/download/mac"
        exit 1
    fi

    TAILSCALE_IP=$(tailscale ip -4 2>/dev/null || true)
    if [ -z "$TAILSCALE_IP" ]; then
        log_error "Tailscale is not connected"
        echo "    Run: tailscale up"
        exit 1
    fi
}

# =============================================================================
# Setup Command - Configure Tailscale-only SSH
# =============================================================================

cmd_setup() {
    print_header
    echo -e "${BOLD}Setting up Tailscale-only SSH server...${NC}"
    echo ""

    require_tailscale
    log_success "Tailscale IP: ${TAILSCALE_IP}"

    if [ "$EUID" -ne 0 ]; then
        log_warn "This command requires sudo. You'll be prompted for your password."
        echo ""
    fi

    # Step 1: Enable Remote Login
    echo -e "\n${YELLOW}[1/5]${NC} Enabling Remote Login (SSH)..."

    SSH_STATUS=$(sudo systemsetup -getremotelogin 2>/dev/null || echo "off")
    if [[ "$SSH_STATUS" == *"On"* ]]; then
        log_success "Remote Login already enabled"
    else
        sudo systemsetup -setremotelogin on
        log_success "Remote Login enabled"
    fi

    # Step 2: Configure SSH to listen only on Tailscale
    echo -e "\n${YELLOW}[2/5]${NC} Configuring SSH to listen only on Tailscale..."

    SSHD_CONFIG="/etc/ssh/sshd_config"
    BACKUP_FILE="/etc/ssh/sshd_config.backup.original"

    if [ ! -f "$BACKUP_FILE" ]; then
        sudo cp "$SSHD_CONFIG" "$BACKUP_FILE"
        log_success "Backed up original sshd_config"
    fi

    sudo mkdir -p /etc/ssh/sshd_config.d

    TAILSCALE_SSH_CONFIG="/etc/ssh/sshd_config.d/tailscale-only.conf"
    sudo tee "$TAILSCALE_SSH_CONFIG" > /dev/null << EOF
# Tailscale-Only SSH Configuration
# Generated by tmux-server-manager
ListenAddress ${TAILSCALE_IP}

# Security settings
PermitRootLogin no
PasswordAuthentication yes
PubkeyAuthentication yes
ChallengeResponseAuthentication no
UsePAM yes

# Keep connections alive
ClientAliveInterval 120
ClientAliveCountMax 3
EOF
    log_success "Created Tailscale SSH config"

    # Ensure Include directive exists
    if ! grep -q "Include /etc/ssh/sshd_config.d" "$SSHD_CONFIG" 2>/dev/null; then
        sudo sed -i '' '1i\
Include /etc/ssh/sshd_config.d/*.conf
' "$SSHD_CONFIG"
        log_success "Added Include directive to sshd_config"
    fi

    # Step 3: Configure pf firewall
    echo -e "\n${YELLOW}[3/5]${NC} Configuring pf firewall..."

    PF_ANCHOR="/etc/pf.anchors/tailscale-ssh"
    sudo tee "$PF_ANCHOR" > /dev/null << 'EOF'
# Tailscale SSH Firewall Rules
# Block SSH on non-Tailscale interfaces, allow from Tailscale network
block in quick on ! utun proto tcp from any to any port 22
pass in quick on utun proto tcp from 100.64.0.0/10 to any port 22
EOF
    log_success "Created pf anchor"

    if ! grep -q "tailscale-ssh" /etc/pf.conf 2>/dev/null; then
        sudo cp /etc/pf.conf "/etc/pf.conf.backup.$(date +%Y%m%d%H%M%S)"
        echo "" | sudo tee -a /etc/pf.conf > /dev/null
        echo "# Tailscale SSH restriction" | sudo tee -a /etc/pf.conf > /dev/null
        echo 'anchor "tailscale-ssh"' | sudo tee -a /etc/pf.conf > /dev/null
        echo 'load anchor "tailscale-ssh" from "/etc/pf.anchors/tailscale-ssh"' | sudo tee -a /etc/pf.conf > /dev/null
        log_success "Added anchor to pf.conf"
    fi

    # Step 4: Create launchd service for pf persistence
    echo -e "\n${YELLOW}[4/5]${NC} Creating launchd service for pf..."

    LAUNCHD_PF="/Library/LaunchDaemons/com.local.tailscale-pf.plist"
    sudo tee "$LAUNCHD_PF" > /dev/null << 'EOF'
<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
    <key>Label</key>
    <string>com.local.tailscale-pf</string>
    <key>ProgramArguments</key>
    <array>
        <string>/sbin/pfctl</string>
        <string>-e</string>
        <string>-f</string>
        <string>/etc/pf.conf</string>
    </array>
    <key>RunAtLoad</key>
    <true/>
</dict>
</plist>
EOF
    sudo chown root:wheel "$LAUNCHD_PF"
    sudo chmod 644 "$LAUNCHD_PF"
    log_success "Created launchd service"

    # Step 5: Apply changes
    echo -e "\n${YELLOW}[5/5]${NC} Applying changes..."

    sudo launchctl unload /System/Library/LaunchDaemons/ssh.plist 2>/dev/null || true
    sudo launchctl load -w /System/Library/LaunchDaemons/ssh.plist
    log_success "Restarted SSH service"

    sudo pfctl -e -f /etc/pf.conf 2>/dev/null || sudo pfctl -f /etc/pf.conf
    log_success "Enabled pf firewall"

    sudo launchctl load "$LAUNCHD_PF" 2>/dev/null || true

    # Summary
    echo ""
    echo -e "${GREEN}╔════════════════════════════════════════════════════════════════╗${NC}"
    echo -e "${GREEN}║${NC}  ${BOLD}Setup Complete!${NC}                                               ${GREEN}║${NC}"
    echo -e "${GREEN}╚════════════════════════════════════════════════════════════════╝${NC}"
    echo ""
    echo "  SSH now listens ONLY on: ${BOLD}${TAILSCALE_IP}:22${NC}"
    echo ""
    echo "  ${YELLOW}To connect from another device:${NC}"
    echo "    ssh $(whoami)@${TAILSCALE_IP}"
    echo "    ssh $(whoami)@$(hostname | tr '[:upper:]' '[:lower:]')"
    echo ""
    echo -e "  ${RED}IMPORTANT:${NC} Keep this terminal open until you verify"
    echo "  you can connect from your other device!"
    echo ""
    echo "  ${YELLOW}If something goes wrong:${NC}"
    echo "    $SCRIPT_NAME rollback"
}

# =============================================================================
# Rollback Command - Restore normal SSH
# =============================================================================

cmd_rollback() {
    print_header
    echo -e "${BOLD}Rolling back Tailscale-only SSH configuration...${NC}"
    echo ""

    if [ "$EUID" -ne 0 ]; then
        log_warn "This command requires sudo. You'll be prompted for your password."
        echo ""
    fi

    # Remove Tailscale SSH config
    if [ -f "/etc/ssh/sshd_config.d/tailscale-only.conf" ]; then
        sudo rm /etc/ssh/sshd_config.d/tailscale-only.conf
        log_success "Removed Tailscale SSH config"
    else
        log_info "No Tailscale SSH config found"
    fi

    # Restore original sshd_config
    if [ -f "/etc/ssh/sshd_config.backup.original" ]; then
        sudo cp /etc/ssh/sshd_config.backup.original /etc/ssh/sshd_config
        log_success "Restored original sshd_config"
    fi

    # Remove pf anchor
    if [ -f "/etc/pf.anchors/tailscale-ssh" ]; then
        sudo rm /etc/pf.anchors/tailscale-ssh
        log_success "Removed pf anchor"
    fi

    # Remove launchd service
    if [ -f "/Library/LaunchDaemons/com.local.tailscale-pf.plist" ]; then
        sudo launchctl unload /Library/LaunchDaemons/com.local.tailscale-pf.plist 2>/dev/null || true
        sudo rm /Library/LaunchDaemons/com.local.tailscale-pf.plist
        log_success "Removed launchd service"
    fi

    # Clean pf.conf
    if grep -q "tailscale-ssh" /etc/pf.conf 2>/dev/null; then
        sudo sed -i '' '/tailscale-ssh/d' /etc/pf.conf
        sudo sed -i '' '/Tailscale SSH restriction/d' /etc/pf.conf
        log_success "Cleaned pf.conf"
    fi

    # Restart services
    sudo launchctl unload /System/Library/LaunchDaemons/ssh.plist 2>/dev/null || true
    sudo launchctl load -w /System/Library/LaunchDaemons/ssh.plist
    log_success "Restarted SSH service"

    sudo pfctl -f /etc/pf.conf 2>/dev/null || true
    log_success "Reloaded pf firewall"

    echo ""
    echo -e "${GREEN}Rollback complete!${NC} SSH now accepts connections from all interfaces."
    echo ""
    echo "Verify with: sudo lsof -i :22 -P -n | grep LISTEN"
}

# =============================================================================
# Status Command - Show server overview
# =============================================================================

cmd_status() {
    print_header

    # System
    echo -e "${BOLD}System${NC}"
    echo "  Hostname:  $(hostname)"
    echo "  Uptime:    $(uptime | sed 's/.*up //' | sed 's/, [0-9]* user.*//')"
    echo "  Load:      $(uptime | sed 's/.*load average: //')"
    echo ""

    # Tailscale
    echo -e "${BOLD}Tailscale${NC}"
    if command -v tailscale &> /dev/null; then
        TAILSCALE_IP=$(tailscale ip -4 2>/dev/null || echo "Not connected")
        TAILSCALE_STATUS=$(tailscale status --self 2>/dev/null | head -1 || echo "Unknown")
        echo "  IP:        $TAILSCALE_IP"
        echo "  Status:    $TAILSCALE_STATUS"
    else
        echo "  Status:    Not installed"
    fi
    echo ""

    # SSH
    echo -e "${BOLD}SSH Server${NC}"
    SSH_LISTEN=$(sudo lsof -i :22 -P -n 2>/dev/null | grep LISTEN | awk '{print $9}' | head -1 || echo "Not running")
    TAILSCALE_ONLY_CONFIG="/etc/ssh/sshd_config.d/tailscale-only.conf"
    if [ -f "$TAILSCALE_ONLY_CONFIG" ]; then
        echo "  Mode:      ${GREEN}Tailscale-only${NC}"
        echo "  Listening: $SSH_LISTEN"
    else
        echo "  Mode:      ${YELLOW}All interfaces${NC}"
        echo "  Listening: $SSH_LISTEN"
    fi
    echo ""

    # pf Firewall
    echo -e "${BOLD}pf Firewall${NC}"
    PF_STATUS=$(sudo pfctl -si 2>/dev/null | grep "^Status:" | awk '{print $2}' || echo "Unknown")
    if [ "$PF_STATUS" = "Enabled" ]; then
        echo "  Status:    ${GREEN}Enabled${NC}"
        if [ -f "/etc/pf.anchors/tailscale-ssh" ]; then
            echo "  SSH Rules: ${GREEN}Active${NC}"
        else
            echo "  SSH Rules: ${YELLOW}Not configured${NC}"
        fi
    else
        echo "  Status:    ${YELLOW}Disabled${NC}"
    fi
    echo ""

    # tmux Sessions
    echo -e "${BOLD}tmux Sessions${NC}"
    SESSION_COUNT=$(tmux list-sessions 2>/dev/null | wc -l | tr -d ' ')
    if [ "$SESSION_COUNT" -gt 0 ]; then
        echo "  Active:    $SESSION_COUNT session(s)"
        tmux list-sessions -F "    - #{session_name}: #{session_windows} window(s), #{?session_attached,attached,detached}" 2>/dev/null
    else
        echo "  Active:    None"
    fi
    echo ""

    # Resources
    echo -e "${BOLD}Resources${NC}"
    echo "  Memory:    $(vm_stat 2>/dev/null | awk '/Pages free/ {free=$3} /Pages active/ {active=$3} END {printf "%.1f GB free", (free*4096)/1073741824}' || echo "Unknown")"
    echo "  Disk:      $(df -h / 2>/dev/null | awk 'NR==2 {print $4 " available"}' || echo "Unknown")"
}

# =============================================================================
# tmux Session Management Commands
# =============================================================================

cmd_list() {
    echo -e "${BOLD}tmux Sessions${NC}"
    echo ""

    if ! tmux list-sessions 2>/dev/null; then
        log_info "No active sessions"
        return
    fi

    echo ""
    for session in $(tmux list-sessions -F "#{session_name}" 2>/dev/null); do
        ATTACHED=$(tmux list-sessions -F "#{session_name} #{session_attached}" | grep "^$session " | awk '{print $2}')
        if [ "$ATTACHED" = "1" ]; then
            echo -e "${GREEN}● $session${NC} (attached)"
        else
            echo -e "${BLUE}○ $session${NC}"
        fi
        tmux list-windows -t "$session" -F "  └─ #{window_index}: #{window_name} (#{pane_current_command})" 2>/dev/null
    done
}

cmd_new() {
    local name="$1"
    if [ -z "$name" ]; then
        log_error "Session name required"
        echo "  Usage: $SCRIPT_NAME new <name>"
        exit 1
    fi

    if tmux has-session -t "$name" 2>/dev/null; then
        log_warn "Session '$name' already exists"
        echo "  Use: $SCRIPT_NAME attach $name"
        exit 1
    fi

    tmux new-session -d -s "$name"
    log_success "Created session: $name"
    echo "  Attach: $SCRIPT_NAME attach $name"
}

cmd_attach() {
    local name="$1"
    if [ -z "$name" ]; then
        # If no name given, attach to most recent or show list
        if tmux list-sessions &>/dev/null; then
            tmux attach
        else
            log_error "No sessions to attach to"
            exit 1
        fi
        return
    fi

    if ! tmux has-session -t "$name" 2>/dev/null; then
        log_error "Session '$name' not found"
        echo "  Available:"
        tmux list-sessions -F "    - #{session_name}" 2>/dev/null || echo "    (none)"
        exit 1
    fi

    tmux attach -t "$name"
}

cmd_kill() {
    local name="$1"
    if [ -z "$name" ]; then
        log_error "Session name required"
        echo "  Usage: $SCRIPT_NAME kill <name>"
        exit 1
    fi

    if [ "$name" = "all" ]; then
        echo -e "${RED}This will kill ALL tmux sessions!${NC}"
        read -p "Continue? (yes/no): " confirm
        if [ "$confirm" = "yes" ]; then
            tmux kill-server 2>/dev/null || true
            log_success "Killed all sessions"
        else
            echo "Aborted"
        fi
        return
    fi

    if ! tmux has-session -t "$name" 2>/dev/null; then
        log_warn "Session '$name' not found"
        exit 1
    fi

    tmux kill-session -t "$name"
    log_success "Killed session: $name"
}

cmd_send() {
    local session="$1"
    shift
    local cmd="$*"

    if [ -z "$session" ] || [ -z "$cmd" ]; then
        log_error "Session and command required"
        echo "  Usage: $SCRIPT_NAME send <session> <command>"
        exit 1
    fi

    if ! tmux has-session -t "$session" 2>/dev/null; then
        log_error "Session '$session' not found"
        exit 1
    fi

    tmux send-keys -t "$session" "$cmd" C-m
    log_success "Sent to '$session': $cmd"
}

cmd_capture() {
    local session="$1"
    local lines="${2:-50}"

    if [ -z "$session" ]; then
        log_error "Session name required"
        echo "  Usage: $SCRIPT_NAME capture <session> [lines]"
        exit 1
    fi

    if ! tmux has-session -t "$session" 2>/dev/null; then
        log_error "Session '$session' not found"
        exit 1
    fi

    echo -e "${BOLD}Last $lines lines from '$session':${NC}"
    echo "─────────────────────────────────────────"
    tmux capture-pane -t "$session" -p -S -"$lines"
    echo "─────────────────────────────────────────"
}

cmd_windows() {
    local session="$1"
    if [ -z "$session" ]; then
        log_error "Session name required"
        exit 1
    fi

    if ! tmux has-session -t "$session" 2>/dev/null; then
        log_error "Session '$session' not found"
        exit 1
    fi

    echo -e "${BOLD}Windows in '$session':${NC}"
    tmux list-windows -t "$session" -F "  #{window_index}: #{window_name} [#{window_width}x#{window_height}] #{?window_active,← active,}"
}

cmd_cleanup() {
    echo -e "${BOLD}Cleaning up idle sessions...${NC}"
    echo ""

    local cleaned=0
    for session in $(tmux list-sessions -F "#{session_name}" 2>/dev/null); do
        local procs=$(tmux list-panes -t "$session" -F "#{pane_current_command}" 2>/dev/null | grep -cv "^bash$\|^zsh$\|^sh$" || echo "0")
        local attached=$(tmux list-sessions -F "#{session_name} #{session_attached}" 2>/dev/null | grep "^$session " | awk '{print $2}')

        if [ "$procs" -eq 0 ] && [ "${attached:-0}" -eq 0 ]; then
            log_info "Killing idle: $session"
            tmux kill-session -t "$session"
            ((cleaned++)) || true
        fi
    done

    if [ "$cleaned" -eq 0 ]; then
        log_success "No cleanup needed"
    else
        log_success "Cleaned $cleaned session(s)"
    fi
}

# =============================================================================
# Help
# =============================================================================

cmd_help() {
    print_header

    echo -e "${BOLD}SETUP & CONFIGURATION${NC}"
    echo "  setup                  Configure Mac as Tailscale-only SSH server"
    echo "  rollback               Restore normal SSH (all interfaces)"
    echo "  status                 Show server status overview"
    echo ""

    echo -e "${BOLD}SESSION MANAGEMENT${NC}"
    echo "  list, ls               List all tmux sessions"
    echo "  new <name>             Create new session"
    echo "  attach, a [name]       Attach to session (or most recent)"
    echo "  kill <name|all>        Kill session(s)"
    echo "  cleanup                Remove idle/empty sessions"
    echo ""

    echo -e "${BOLD}SESSION INTERACTION${NC}"
    echo "  send <session> <cmd>   Send command to session"
    echo "  capture <session> [n]  Show last n lines (default: 50)"
    echo "  windows <session>      List windows in session"
    echo ""

    echo -e "${BOLD}EXAMPLES${NC}"
    echo "  $SCRIPT_NAME setup                    # First-time setup"
    echo "  $SCRIPT_NAME new dev-server           # Create session"
    echo "  $SCRIPT_NAME send dev-server 'npm start'"
    echo "  $SCRIPT_NAME capture dev-server 100   # View output"
    echo "  $SCRIPT_NAME attach dev-server        # Interactive"
    echo ""

    echo -e "${BOLD}REMOTE CONNECTION${NC}"
    echo "  After setup, connect from another Tailscale device:"
    echo "    ssh user@<tailscale-ip>"
    echo "    ssh user@<tailscale-hostname>"
}

# =============================================================================
# Main
# =============================================================================

case "${1:-}" in
    setup)
        cmd_setup
        ;;
    rollback|restore)
        cmd_rollback
        ;;
    status|st)
        cmd_status
        ;;
    list|ls)
        cmd_list
        ;;
    new|create)
        cmd_new "$2"
        ;;
    attach|a)
        cmd_attach "$2"
        ;;
    kill|rm)
        cmd_kill "$2"
        ;;
    send)
        shift
        cmd_send "$@"
        ;;
    capture|cap)
        cmd_capture "$2" "$3"
        ;;
    windows|win)
        cmd_windows "$2"
        ;;
    cleanup)
        cmd_cleanup
        ;;
    help|--help|-h)
        cmd_help
        ;;
    version|--version|-v)
        echo "tmux-server-manager v${VERSION}"
        ;;
    "")
        cmd_help
        ;;
    *)
        log_error "Unknown command: $1"
        echo "  Run '$SCRIPT_NAME help' for usage"
        exit 1
        ;;
esac
